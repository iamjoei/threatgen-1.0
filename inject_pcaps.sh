#!/bin/bash
# #################################################################################################################
# 
# #################################################################################################################

# #################################################################################################################
# NETWORK INTERFACE SETUP
# #################################################################################################################
modprobe dummy
ifconfig dummy0 up
ifconfig dummy0 promisc

# #################################################################################################################
# TRAFFIC REPLAY EXECUTION
# #################################################################################################################
SCRIPTPATH=$( cd $(dirname $0) ; pwd -P )
cd $SCRIPTPATH/pcaps

for pcap in `ls *.pcap`
do
	tcpreplay-edit -T nano -N '10.0.0.0/8:192.168.69.21/30,192.168.0.0/16:192.168.69.121/28' -i dummy0 --pps=10 $pcap
done
